package Modelo;

/**
 *
 * @author User
 */
public class Vehiculo {
    int id;
    String placa;
    String tipo;
    String color;
    String modelo;

    public Vehiculo() {
    }

    public Vehiculo(int id, String placa, String tipo, String color, String modelo) {
        this.id = id;
        this.placa = placa;
        this.tipo = tipo;
        this.color = color;
        this.modelo = modelo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }
    
    
}
